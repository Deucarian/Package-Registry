def build_report(args: argparse.Namespace) -> dict[str, Any]:
    output_root = args.output_root.resolve()
    audit_root = (args.audit_root or repo_root_default(output_root)).resolve()
    registry = load_registry(output_root)
    include = {name.lower() for name in args.include_repository}
    exclude = {name.lower() for name in args.exclude_repository}

    analyzer = CSharpSyntaxAnalyzer(authoritative=args.authoritative)
    repositories: list[dict[str, Any]] = []
    parsed_files: list[ParsedFile] = []
    parse_failures: list[dict[str, Any]] = []

    for repo_root in sorted([p for p in audit_root.iterdir() if p.is_dir()], key=lambda p: p.name.lower()):
        if include and repo_root.name.lower() not in include:
            continue
        if repo_root.name.lower() in exclude:
            continue
        package_json = read_json(repo_root / "package.json") or {}
        asmdefs = collect_asmdefs(repo_root)
        repo = {
            "name": repo_root.name,
            "canonicalId": f"{args.organization}/{repo_root.name}",
            "packageId": package_json.get("name"),
            "packageVersion": package_json.get("version"),
            "unity": package_json.get("unity"),
            "displayName": package_json.get("displayName"),
            "branches": collect_branch_info(repo_root),
            "dependencies": package_json.get("dependencies", {}) or {},
            "asmdefs": asmdefs,
            "registryDependencies": (registry.get(package_json.get("name")) or {}).get("dependencies") if package_json.get("name") else None,
            "registryGroupId": (registry.get(package_json.get("name")) or {}).get("groupId") if package_json.get("name") else None,
            "ciWorkflows": sorted(rel(p, repo_root) for p in (repo_root / ".github" / "workflows").glob("*.y*ml")) if (repo_root / ".github" / "workflows").exists() else [],
            "validationScripts": sorted(rel(p, repo_root) for p in repo_root.glob("Tools/**/*") if p.is_file() and "validat" in p.name.lower()),
            "samples": sorted(rel(p, repo_root) for p in repo_root.glob("Samples~/**/*") if p.is_file())[:80],
        }
        repositories.append(repo)

        for path in sorted(repo_root.rglob("*.cs")):
            relative_path = rel(path, repo_root)
            if any(part in SKIP_DIRS for part in relative_path.split("/")):
                continue
            text = read_text(path)
            parsed = ParsedFile(
                repo=repo_root.name,
                package_id=repo.get("packageId"),
                repo_root=repo_root,
                path=path,
                relative_path=relative_path,
                scope=scope_for(relative_path),
                assembly=assembly_for(relative_path, asmdefs),
                text=text,
                generated=is_generated_file(path, text),
            )
            analyzer.parse_file(parsed)
            if parsed.parse_error and parsed.parse_error != "generated-code-excluded":
                parse_failures.append({"repository": parsed.repo, "file": parsed.relative_path, "error": parsed.parse_error})
            parsed_files.append(parsed)

    global parsed_repo_dependencies, repo_asm_refs
    parsed_repo_dependencies = {repo["name"]: repo.get("dependencies", {}) for repo in repositories}
    repo_asm_refs = {
        repo["name"]: set(ref for asm in repo.get("asmdefs", []) for ref in asm.get("references", []))
        for repo in repositories
    }
    # Recollect invocations now dependency globals are available for records.
    for parsed in parsed_files:
        parsed.debug_invocations.clear()
        parsed.lifetime_occurrences.clear()
        if parsed.tree is not None:
            analyzer._collect_invocations(parsed, parsed.tree.root_node, parsed.text.encode("utf-8"))

    if args.authoritative and parse_failures:
        raise RuntimeError(f"Authoritative parse failed for {len(parse_failures)} C# files.")

    all_symbols = [symbol for parsed in parsed_files for symbol in parsed.symbols]
    production_api = [symbol for symbol in all_symbols if symbol.get("externallyPublic")]
    public_missing_xml = [symbol for symbol in production_api if not symbol.get("xmlDocumentation")]
    runtime_api = [symbol for symbol in production_api if symbol["scope"] == "Runtime production"]
    editor_api = [symbol for symbol in production_api if symbol["scope"] == "Editor production"]
    test_public = [symbol for symbol in all_symbols if symbol["scope"] == "Test" and symbol["accessibility"] == "public"]
    sample_public = [symbol for symbol in all_symbols if symbol["scope"] == "Sample" and symbol["accessibility"] == "public"]
    internal_private = [symbol for symbol in all_symbols if is_production_scope(symbol["scope"]) and not symbol.get("externallyPublic")]
    methods = [method for parsed in parsed_files for method in parsed.methods]
    exact_clones, structural_clones, semantic_candidates = clone_groups(methods)

    package_to_assemblies = defaultdict(set)
    for repo in repositories:
        if repo.get("packageId"):
            for asm in repo.get("asmdefs", []):
                if asm.get("name"):
                    package_to_assemblies[repo["packageId"]].add(asm["name"])

    debug_records = [record for parsed in parsed_files for record in parsed.debug_invocations]
    for repo in repositories:
        repo_root = audit_root / repo["name"]
        debug_records.extend(markdown_debug_examples(repo, repo_root))
    debug_records.sort(key=lambda item: (item["repository"], item["file"], item["line"], item.get("invocation", "")))

    lifetime_records = [record for parsed in parsed_files for record in parsed.lifetime_occurrences]
    lifetime_records.extend(helper_definition_records(parsed_files))
    lifetime_records.sort(key=lambda item: (item["repository"], item["file"], item["line"], item.get("occurrenceKind", "")))

    documentation_records = []
    dependency_usage_records = []
    for repo in repositories:
        repo_root = audit_root / repo["name"]
        registry_package = registry.get(repo.get("packageId"))
        documentation_records.extend(documentation_drift(repo, repo_root, registry_package))
        dependency_usage_records.extend(dependency_usage(repo, [p for p in parsed_files if p.repo == repo["name"]], package_to_assemblies, registry_package))
    documentation_records.sort(key=lambda item: (item["repository"], item["kind"], item.get("file", ""), item.get("dependency", "")))
    dependency_usage_records.sort(key=lambda item: (item["repository"], item.get("dependency", item.get("assemblyReference", ""))))

    edges = []
    versions_by_pkg = {repo["packageId"]: repo["packageVersion"] for repo in repositories if repo.get("packageId")}
    version_drifts = []
    registry_drifts = []
    for repo in repositories:
        for dep, version in sorted((repo.get("dependencies") or {}).items()):
            if dep.startswith("com.deucarian."):
                edges.append({"from": repo.get("packageId"), "to": dep, "version": version, "fromRepo": repo["name"], "toRepo": next((r["name"] for r in repositories if r.get("packageId") == dep), None)})
                owner_version = versions_by_pkg.get(dep)
                if owner_version and version != owner_version:
                    version_drifts.append({"repo": repo["name"], "packageId": repo.get("packageId"), "dependency": dep, "declaredVersion": version, "ownerDevelopVersion": owner_version})
        if repo.get("registryDependencies") is not None:
            pkg_deps = sorted(dep for dep in (repo.get("dependencies") or {}) if dep.startswith("com.deucarian."))
            reg_deps = sorted(repo.get("registryDependencies") or [])
            if pkg_deps != reg_deps:
                registry_drifts.append({"repo": repo["name"], "packageId": repo.get("packageId"), "packageJsonDependencies": pkg_deps, "registryDependencies": reg_deps})

    capability_matrix = []
    for cap_id, owner, desc in CAPABILITIES:
        owner_repo = next((repo["name"] for repo in repositories if repo.get("packageId") == owner), None)
        consumers = sorted(set(edge["from"] for edge in edges if edge["to"] == owner))
        capability_matrix.append({"id": cap_id, "ownerPackageId": owner, "ownerRepository": owner_repo or ("Package-Registry" if cap_id == "registry-metadata" else None), "description": desc, "currentConsumers": consumers})

    lifetime_conclusion = classify_lifetime_conclusion(lifetime_records)
    extraction_decisions = reviewed_extraction_decisions(exact_clones, structural_clones, semantic_candidates, lifetime_conclusion)

    return {
        "metadata": {
            "schemaVersion": 2,
            "organization": args.organization,
            "ref": args.ref,
            "parser": "tree-sitter-c-sharp" if analyzer.available else "unavailable",
            "authoritative": args.authoritative,
            "generatedAtUtc": "not-recorded-deterministic-output",
        },
        "repositories": repositories,
        "parseFailures": parse_failures,
        "publicApi": {
            "runtimeProductionCount": len(runtime_api),
            "editorProductionCount": len(editor_api),
            "testsPublicCount": len(test_public),
            "samplesPublicCount": len(sample_public),
            "internalPrivateProductionCount": len(internal_private),
            "runtimeProduction": sort_symbols(runtime_api),
            "editorProduction": sort_symbols(editor_api),
            "tests": sort_symbols(test_public),
            "samples": sort_symbols(sample_public),
            "internalPrivate": sort_symbols(internal_private),
            "symbols": sort_symbols(production_api),
            "missingXmlDocumentation": sort_symbols(public_missing_xml),
        },
        "duplication": {
            "methodCountAnalyzed": len(methods),
            "exactAstCloneGroups": exact_clones,
            "normalizedStructuralCloneGroups": structural_clones,
            "sameSymbolSemanticCandidates": semantic_candidates,
            "similarLookingButSemanticallyDifferent": [],
        },
        "debugApi": {
            "invocations": debug_records,
            "summary": summarize_by(debug_records, "initialDisposition"),
        },
        "unityObjectLifetime": {
            "occurrences": lifetime_records,
            "summary": summarize_by(lifetime_records, "occurrenceKind"),
            "conclusion": lifetime_conclusion,
        },
        "documentationDrift": {
            "findings": documentation_records,
            "summary": summarize_by(documentation_records, "kind"),
        },
        "dependencyUsage": {
            "findings": dependency_usage_records,
            "summary": summarize_by(dependency_usage_records, "classification"),
        },
        "dependencyGraph": {"edges": sorted(edges, key=lambda e: (e.get("from") or "", e.get("to") or "")), "cycles": detect_cycles(edges)},
        "drift": {"dependencyVersionDrift": sorted(version_drifts, key=lambda x: (x["repo"], x["dependency"])), "registryDependencyDrift": sorted(registry_drifts, key=lambda x: x["repo"])},
        "capabilityMatrix": capability_matrix,
        "extractionDecisions": extraction_decisions,
    }


def summarize_by(records: list[dict[str, Any]], key: str) -> dict[str, int]:
    counts: dict[str, int] = defaultdict(int)
    for record in records:
        counts[str(record.get(key, "(none)"))] += 1
    return dict(sorted(counts.items()))


def sort_symbols(symbols: Iterable[dict[str, Any]]) -> list[dict[str, Any]]:
    return sorted(symbols, key=lambda symbol: (symbol["repository"], symbol["file"], symbol["line"], symbol["symbol"]))


def detect_cycles(edges: list[dict[str, Any]]) -> list[list[str]]:
    graph: dict[str, list[str]] = defaultdict(list)
    for edge in edges:
        if edge.get("from") and edge.get("to"):
            graph[edge["from"]].append(edge["to"])
    cycles: list[list[str]] = []
    stack: list[str] = []
    seen: set[str] = set()

    def visit(node: str, visiting: set[str]) -> None:
        if node in visiting:
            cycle = stack[stack.index(node) :] + [node]
            if cycle not in cycles:
                cycles.append(cycle)
            return
        if node in seen:
            return
        visiting.add(node)
        stack.append(node)
        for nxt in sorted(graph.get(node, [])):
            visit(nxt, visiting)
        stack.pop()
        visiting.remove(node)
        seen.add(node)

    for node in sorted(graph):
        visit(node, set())
    return cycles


def classify_lifetime_conclusion(records: list[dict[str, Any]]) -> dict[str, Any]:
    helper_defs = [r for r in records if r.get("occurrenceKind") == "helper definition"]
    runtime = [r for r in records if r.get("scope") == "Runtime production"]
    editor = [r for r in records if r.get("scope") == "Editor production"]
    tests = [r for r in records if r.get("scope") == "Test"]
    if runtime and editor:
        decision = "multiple scope-specific owners required"
    elif runtime and len({r["repository"] for r in runtime}) >= 3:
        decision = "runtime Common package justified"
    elif editor:
        decision = "Editor-owned helper justified"
    elif tests and len({r["repository"] for r in tests}) >= 3:
        decision = "Testing-owned helper justified"
    else:
        decision = "no extraction justified"
    return {
        "decision": decision,
        "helperDefinitionCount": len(helper_defs),
        "runtimeRepositoryCount": len({r["repository"] for r in runtime}),
        "editorRepositoryCount": len({r["repository"] for r in editor}),
        "testRepositoryCount": len({r["repository"] for r in tests}),
        "note": "No package is created by this audit wave; this conclusion directs the next design review.",
    }


def reviewed_extraction_decisions(exact: list[dict[str, Any]], structural: list[dict[str, Any]], semantic: list[dict[str, Any]], lifetime: dict[str, Any]) -> list[dict[str, Any]]:
    decisions = []
    for group in structural[:50]:
        if group["extractionConfidence"] == "medium":
            decision = "Needs design review"
        else:
            decision = "Keep local"
        decisions.append({"candidate": group["groupKey"], "category": group["category"], "repositories": group["repositories"], "decision": decision, "reasoning": "; ".join(group["reasonsNotToExtract"] or group["reasonsToExtract"] or ["Manual semantic review required."])})
    for group in semantic[:50]:
        decisions.append({"candidate": group["groupKey"], "category": group["category"], "repositories": group["repositories"], "decision": "Needs design review", "reasoning": "Same symbol names are not enough to extract; review semantics and owner capability."})
    decisions.append({"candidate": "unity-object-lifetime", "category": "dedicated audit", "repositories": [], "decision": lifetime["decision"], "reasoning": lifetime["note"]})
    return sorted(decisions, key=lambda item: (item["category"], item["candidate"]))


def md_table(headers: list[str], rows: Iterable[Iterable[Any]]) -> str:
    out = ["| " + " | ".join(headers) + " |", "| " + " | ".join(["---"] * len(headers)) + " |"]
    for row in rows:
        out.append("| " + " | ".join(str(cell).replace("\n", " ").replace("|", "\\|") for cell in row) + " |")
    return "\n".join(out)


def write_artifacts(report: dict[str, Any], output_root: Path, formats: str = "all") -> None:
    output_root.mkdir(parents=True, exist_ok=True)
    json_enabled = formats in {"all", "json"}
    md_enabled = formats in {"all", "markdown"}

    if json_enabled:
        write_json(output_root / "DUPLICATION_REPORT.json", report)
        write_json(output_root / "DEBUG_API_AUDIT.json", report["debugApi"])
        write_json(output_root / "UNITY_OBJECT_LIFETIME_AUDIT.json", report["unityObjectLifetime"])
        write_json(output_root / "DOCUMENTATION_DRIFT.json", report["documentationDrift"])
        write_json(output_root / "DEPENDENCY_USAGE_AUDIT.json", report["dependencyUsage"])
        write_json(output_root / "capabilities.json", capabilities_json(report))
        write_json(output_root / "dependency-rules.json", dependency_rules_json(report))

    if not md_enabled:
        return

    write_reuse_audit(report, output_root / "REUSE_AUDIT.md")
    write_capability_matrix(report, output_root / "PACKAGE_CAPABILITY_MATRIX.md")
    write_dependency_graph(report, output_root / "DEPENDENCY_GRAPH.md")
    write_migration_plan(report, output_root / "MIGRATION_PLAN.md")
    write_debug_md(report, output_root / "DEBUG_API_AUDIT.md")
    write_lifetime_md(report, output_root / "UNITY_OBJECT_LIFETIME_AUDIT.md")
    write_doc_drift_md(report, output_root / "DOCUMENTATION_DRIFT.md")
    write_dependency_usage_md(report, output_root / "DEPENDENCY_USAGE_AUDIT.md")
    write_extraction_decisions_md(report, output_root / "EXTRACTION_DECISIONS.md")


def capabilities_json(report: dict[str, Any]) -> dict[str, Any]:
    caps = []
    for cap in report["capabilityMatrix"]:
        item = {"id": cap["id"], "ownerPackageId": cap.get("ownerPackageId"), "description": cap["description"]}
        if cap["id"] == "logging":
            item["forbiddenSymbolsOutsideOwner"] = DEBUG_FORBIDDEN_SYMBOLS
            item["exceptions"] = [
                {"packageId": "com.deucarian.bootstrap", "reason": "First-time self-contained bootstrap."},
                {"packageId": "com.deucarian.logging", "reason": "Owns Unity console forwarding and sink validation."},
            ]
        if cap["id"] == "unity-object-lifetime":
            item["status"] = report["unityObjectLifetime"]["conclusion"]["decision"]
        caps.append(item)
    return {"schemaVersion": 2, "capabilities": caps}


