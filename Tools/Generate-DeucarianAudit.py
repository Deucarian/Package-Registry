@dataclass(order=True)
class SymbolContext:
    namespace: str = ""
    containing_type: str = ""
    symbol: str = ""
    kind: str = ""
    accessibility: str = ""
    effective_public: bool = False


@dataclass
class ParsedFile:
    repo: str
    package_id: str | None
    repo_root: Path
    path: Path
    relative_path: str
    scope: str
    assembly: str
