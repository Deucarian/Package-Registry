def run(cmd: list[str], cwd: Path | None = None, timeout: int = 60) -> tuple[str, str, int]:
    if cmd and Path(cmd[0]).name.lower() in {"git", "git.exe"}:
        cmd = [cmd[0], "-c", "core.longpaths=true", *cmd[1:]]
    try:
        result = subprocess.run(cmd, cwd=cwd, capture_output=True, text=True, timeout=timeout)
        return result.stdout.strip(), result.stderr.strip(), result.returncode
    except Exception as exc:
        return "", str(exc), 1


def read_text(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8-sig")
    except UnicodeDecodeError:
        return path.read_text(encoding="utf-8", errors="replace")
    except Exception:
        return ""


def read_json(path: Path) -> Any:
    try:
        return json.loads(read_text(path))
    except Exception:
        return None


def file_sha256(path: Path) -> str:
    if not path.is_file():
        return ""
    return hashlib.sha256(path.read_bytes()).hexdigest()


def package_id_version_mentions(text: str, package_id: str) -> list[str]:
    """Return nearby semantic versions for exact package-id mentions only."""
    pattern = re.compile(
        rf"(?<![{PACKAGE_ID_BOUNDARY_CHARS}])"
        rf"{re.escape(package_id)}"
        rf"(?![{PACKAGE_ID_BOUNDARY_CHARS}])"
        rf"[^\n\r0-9]{{0,80}}?"
        rf"(\d+\.\d+\.\d+)",
        flags=re.I,
    )
    return sorted(set(pattern.findall(text)))


def write_json(path: Path, data: Any) -> None:
    path.write_text(json.dumps(sort_value(data), indent=2, ensure_ascii=True) + "\n", encoding="utf-8")


def sort_value(value: Any) -> Any:
    if isinstance(value, dict):
        return {key: sort_value(value[key]) for key in sorted(value)}
    if isinstance(value, list):
        return [sort_value(item) for item in value]
    return value


def rel(path: Path, root: Path) -> str:
    return path.relative_to(root).as_posix()


def iter_text_files(root: Path) -> Iterable[Path]:
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = sorted(d for d in dirnames if d not in SKIP_DIRS)
        for filename in sorted(filenames):
            path = Path(dirpath) / filename
            if path.suffix in TEXT_EXTS:
                yield path


def path_fallback_scope(relative_path: str) -> str:
    first = relative_path.split("/", 1)[0]
    if first in {"Tools", ".github"}:
        return "Build/CI"
    if first.startswith("Samples"):
        return "Sample"
    if first == "Runtime" or first.startswith("Runtime."):
        return "Runtime production"
    if first == "Editor" or first.startswith("Editor."):
        return "Editor production"
    if first == "Tests" or first.startswith("Tests."):
        return "Test"
    return "Other"


def assembly_roles(governance: dict[str, Any] | None) -> dict[str, str]:
    governance = governance or {}
    roles: dict[str, str] = {}
    for key, scope in (
        ("runtimeAssemblies", "Runtime production"),
        ("editorAssemblies", "Editor production"),
        ("sampleAssemblies", "Sample"),
        ("testAssemblies", "Test"),
    ):
        for assembly in governance.get(key, []) or []:
            if isinstance(assembly, str) and assembly:
                roles[assembly] = scope
    return roles


def scope_for(relative_path: str, asmdef: dict[str, Any] | None = None, governance: dict[str, Any] | None = None) -> str:
    fallback = path_fallback_scope(relative_path)
    if fallback in {"Sample", "Build/CI"}:
        return fallback
    if asmdef:
        configured_scope = assembly_roles(governance).get(asmdef.get("name", ""))
        if configured_scope:
            return configured_scope
        if is_test_asmdef(asmdef):
            return "Test"
        if "Editor" in asmdef.get("includePlatforms", []):
            return "Editor production"
        return "Runtime production"
    return fallback


def is_test_asmdef(asmdef: dict[str, Any]) -> bool:
    name = asmdef.get("name", "")
    path = asmdef.get("path", "")
    optional_refs = set(asmdef.get("optionalUnityReferences", []))
    return (
        "TestAssemblies" in optional_refs
        or ".Tests" in name
        or name.endswith(".Tests")
        or path.startswith("Tests/")
        or path.startswith("Tests.")
        or "/Tests/" in path
    )


def owning_asmdef(relative_path: str, asmdefs: list[dict[str, Any]]) -> dict[str, Any] | None:
    best_prefix = ""
    best_asmdef: dict[str, Any] | None = None
    for asm in asmdefs:
        prefix = str(Path(asm["path"]).parent).replace("\\", "/")
        if prefix == ".":
            prefix = ""
        if not prefix or relative_path.startswith(prefix.rstrip("/") + "/"):
            if len(prefix) >= len(best_prefix):
                best_prefix = prefix
                best_asmdef = asm
    return best_asmdef


def assembly_for(relative_path: str, asmdefs: list[dict[str, Any]]) -> str:
    asmdef = owning_asmdef(relative_path, asmdefs)
    return asmdef.get("name", "") if asmdef else ""


def node_text(node: Any, source: bytes) -> str:
    return source[node.start_byte : node.end_byte].decode("utf-8", errors="replace")


def child_text(node: Any, field: str, source: bytes) -> str:
    child = node.child_by_field_name(field)
    return node_text(child, source) if child is not None else ""


def walk(node: Any) -> Iterable[Any]:
    yield node
    for child in node.children:
        yield from walk(child)


def direct_children(node: Any, types: set[str]) -> list[Any]:
    return [child for child in node.children if child.type in types]


def modifier_texts(node: Any, source: bytes) -> list[str]:
    return [node_text(child, source) for child in node.children if child.type == "modifier"]


def accessibility(node: Any, source: bytes, default: str = "private") -> str:
    modifiers = modifier_texts(node, source)
    for candidate in ("public", "protected internal", "protected", "internal", "private"):
        if candidate in " ".join(modifiers):
            return candidate
    return default


def is_generated_file(path: Path, text: str) -> bool:
    name = path.name.lower()
    return name.endswith(".g.cs") or name.endswith(".generated.cs") or name.endswith(".designer.cs") or any(marker.lower() in text[:1200].lower() for marker in GENERATED_MARKERS)


def parser_source_with_contextual_identifier_recovery(text: str) -> str:
    """Rewrite valid lowercase `required` identifiers for the parser only.

    tree-sitter-c-sharp currently treats some uses of the contextual `required`
    keyword as syntax errors. The equal-byte-length sentinel preserves every
    source offset, and the look-ahead deliberately leaves `required T Member`
    modifiers unchanged.
    """
    return re.sub(
        r"\brequired\b(?=\s*(?:[=;,.)?\[\]}:+\-*/%&|]|$))",
        "requireD",
        text,
    )


def is_production_scope(scope: str) -> bool:
    return scope in {"Runtime production", "Editor production"}


class CSharpSyntaxAnalyzer:
    declaration_types = {
        "class_declaration",
        "struct_declaration",
        "record_declaration",
        "record_struct_declaration",
        "interface_declaration",
        "enum_declaration",
        "delegate_declaration",
        "method_declaration",
        "constructor_declaration",
        "destructor_declaration",
        "local_function_statement",
        "property_declaration",
        "indexer_declaration",
        "event_declaration",
        "event_field_declaration",
        "operator_declaration",
        "conversion_operator_declaration",
    }
    type_declarations = {
        "class_declaration",
        "struct_declaration",
        "record_declaration",
        "record_struct_declaration",
        "interface_declaration",
        "enum_declaration",
        "delegate_declaration",
    }
    body_declarations = {
        "method_declaration",
        "constructor_declaration",
        "destructor_declaration",
        "local_function_statement",
        "operator_declaration",
        "conversion_operator_declaration",
        "property_declaration",
        "indexer_declaration",
    }

    def __init__(self, authoritative: bool = False):
        self.authoritative = authoritative
        self.available = Parser is not None and CSHARP_LANGUAGE is not None
        if self.available:
            self.parser = Parser()
            self.parser.language = CSHARP_LANGUAGE
        elif authoritative:
            raise RuntimeError("tree_sitter and tree_sitter_c_sharp are required for authoritative C# auditing.")

    def parse_file(self, parsed: ParsedFile) -> None:
        if parsed.generated:
            parsed.parse_error = "generated-code-excluded"
            return
        if not self.available:
            parsed.parse_error = "syntax-parser-unavailable"
            return

        source = parsed.text.encode("utf-8")
        parsed.tree = self.parser.parse(source)
        if parsed.tree.root_node.has_error:
            recovered_text = parser_source_with_contextual_identifier_recovery(parsed.text)
            if recovered_text != parsed.text:
                recovered_tree = self.parser.parse(recovered_text.encode("utf-8"))
                if not recovered_tree.root_node.has_error:
                    parsed.tree = recovered_tree
                    parsed.parser_recoveries.append("contextual-required-identifier")
        if parsed.tree.root_node.has_error:
            parsed.parse_error = "tree-sitter-parse-error"
            if self.authoritative:
                raise RuntimeError(f"C# parse error in {parsed.repo}/{parsed.relative_path}")

        parsed.aliases = self._find_aliases(parsed.tree.root_node, source)
        parsed.using_unity_engine = self._has_using_unity_engine(parsed.tree.root_node, source)
        self._collect_symbols(parsed, parsed.tree.root_node, source)
        self._collect_invocations(parsed, parsed.tree.root_node, source)

    def _find_aliases(self, root: Any, source: bytes) -> dict[str, str]:
        aliases: dict[str, str] = {}
        for node in walk(root):
            if node.type not in {"using_alias_directive", "using_directive"}:
                continue
            text = node_text(node, source)
            if "=" not in text:
                continue
            match = re.match(r"\s*using\s+([A-Za-z_][A-Za-z0-9_]*)\s*=\s*([^;]+);", text)
            if match:
                aliases[match.group(1)] = match.group(2).strip()
        return aliases

    def _has_using_unity_engine(self, root: Any, source: bytes) -> bool:
        for node in walk(root):
            if node.type == "using_directive" and node_text(node, source).strip() == "using UnityEngine;":
                return True
        return False

    def _collect_symbols(self, parsed: ParsedFile, root: Any, source: bytes) -> None:
        namespace_stack: list[str] = []
        type_stack: list[SymbolContext] = []

        def visit(node: Any) -> None:
            pushed_ns = False
            pushed_type = False
            if node.type in {"namespace_declaration", "file_scoped_namespace_declaration"}:
                name = child_text(node, "name", source)
                if name:
                    namespace_stack.append(name)
                    pushed_ns = True
            if node.type in self.declaration_types:
                kind = self._symbol_kind(node)
                name = self._symbol_name(node, source)
                access = accessibility(node, source, default="internal" if node.type in self.type_declarations else "private")
                containing_public = all(ctx.effective_public for ctx in type_stack) if type_stack else True
                type_public = access == "public" and containing_public
                member_public = access == "public" and containing_public and (not type_stack or type_stack[-1].effective_public)
                effective_public = type_public if node.type in self.type_declarations else member_public
                context = SymbolContext(
                    namespace=".".join(namespace_stack),
                    containing_type=".".join(ctx.symbol for ctx in type_stack),
                    symbol=name,
                    kind=kind,
                    accessibility=access,
                    effective_public=effective_public,
                )
                symbol_record = {
                    "repository": parsed.repo,
                    "packageId": parsed.package_id,
                    "assembly": parsed.assembly,
                    "scope": parsed.scope,
                    "namespace": context.namespace,
                    "containingType": context.containing_type,
                    "symbolKind": kind,
                    "symbol": name,
                    "fullSignature": self._signature(node, source),
                    "file": parsed.relative_path,
                    "line": node.start_point[0] + 1,
                    "xmlDocumentation": self._has_xml_doc(node, source),
                    "obsolete": self._has_attribute(node, source, "Obsolete"),
                    "accessibility": access,
                    "externallyPublic": effective_public and is_production_scope(parsed.scope),
                }
                parsed.symbols.append(symbol_record)
                if node.type in self.body_declarations:
                    method = self._method_record(parsed, node, source, context)
                    if method:
                        parsed.methods.append(method)
                if node.type in self.type_declarations:
                    type_stack.append(context)
                    pushed_type = True

            for child in node.children:
                visit(child)

            if pushed_type:
                type_stack.pop()
            if pushed_ns:
                namespace_stack.pop()

        visit(root)

    def _symbol_kind(self, node: Any) -> str:
        return {
            "class_declaration": "class",
            "struct_declaration": "struct",
            "record_declaration": "record",
            "record_struct_declaration": "record struct",
            "interface_declaration": "interface",
            "enum_declaration": "enum",
            "delegate_declaration": "delegate",
            "method_declaration": "method",
            "constructor_declaration": "constructor",
            "destructor_declaration": "destructor",
            "local_function_statement": "local function",
            "property_declaration": "property",
            "indexer_declaration": "indexer",
            "event_declaration": "event",
            "event_field_declaration": "event",
            "operator_declaration": "operator",
            "conversion_operator_declaration": "conversion operator",
        }.get(node.type, node.type)

    def _symbol_name(self, node: Any, source: bytes) -> str:
        if node.type == "indexer_declaration":
            return "this[]"
        if node.type == "operator_declaration":
            return "operator " + child_text(node, "operator", source)
        if node.type == "conversion_operator_declaration":
            return "conversion operator"
        name = child_text(node, "name", source)
        if name:
            return name
        for child in node.children:
            if child.type == "identifier":
                return node_text(child, source)
        return self._symbol_kind(node)

    def _signature(self, node: Any, source: bytes) -> str:
        text = node_text(node, source)
        for marker in ("{", "=>", ";"):
            idx = text.find(marker)
            if idx >= 0:
                text = text[:idx]
                break
        return " ".join(text.split())

    def _has_xml_doc(self, node: Any, source: bytes) -> bool:
        prefix = source[: node.start_byte].decode("utf-8", errors="replace")
        lines = prefix.splitlines()[-4:]
        return any(line.strip().startswith("///") for line in lines)

    def _has_attribute(self, node: Any, source: bytes, attr_name: str) -> bool:
        for child in node.children:
            if child.type == "attribute_list" and attr_name in node_text(child, source):
                return True
        return False

    def _method_record(self, parsed: ParsedFile, node: Any, source: bytes, context: SymbolContext) -> dict[str, Any] | None:
        body_nodes = [child for child in node.children if child.type in {"block", "arrow_expression_clause", "accessor_list"}]
        if not body_nodes:
            return None
        body_text = "\n".join(node_text(body, source) for body in body_nodes)
        if len(body_text.strip()) < 8:
            return None
        local_names = self._local_identifiers(node, source)
        normalized = normalize_body(body_text, local_names)
        if len(normalized) < 60:
            return None
        dependencies = sorted(set(re.findall(r"\b(?:UnityEngine|UnityEditor|System|Newtonsoft|Deucarian)\.[A-Za-z0-9_.]+", body_text)))
        return {
            "repository": parsed.repo,
            "packageId": parsed.package_id,
            "assembly": parsed.assembly,
            "scope": parsed.scope,
            "namespace": context.namespace,
            "containingType": context.containing_type,
            "symbol": context.symbol,
            "symbolKind": context.kind,
            "signature": self._signature(node, source),
            "file": parsed.relative_path,
            "line": node.start_point[0] + 1,
            "lineCount": max(1, node.end_point[0] - node.start_point[0] + 1),
            "dependenciesUsed": dependencies,
            "normalizedHash": hashlib.sha256(normalized.encode("utf-8")).hexdigest(),
            "normalizedStructural": normalized,
            "async": "async" in modifier_texts(node, source),
            "iterator": "yield" in body_text,
            "throws": "throw" in body_text,
            "editorApi": "UnityEditor" in body_text,
            "runtimeUnityApi": "UnityEngine" in body_text or "Object." in body_text,
        }

    def _local_identifiers(self, node: Any, source: bytes) -> set[str]:
        names: set[str] = set()
        for child in walk(node):
            if child.type == "parameter":
                name = child.child_by_field_name("name")
                if name is not None:
                    names.add(node_text(name, source))
            elif child.type == "variable_declarator":
                name = child.child_by_field_name("name")
                if name is not None:
                    names.add(node_text(name, source))
        return names

    def _collect_invocations(self, parsed: ParsedFile, root: Any, source: bytes) -> None:
        symbol_stack = self._symbol_ranges(parsed.symbols)
        for node in walk(root):
            if node.type != "invocation_expression":
                continue
            function = node.child_by_field_name("function")
            if function is None:
                continue
            invocation_name = self._invocation_name(function, source)
            containing_symbol = self._containing_symbol(symbol_stack, node.start_point[0] + 1)
            if self._is_debug_invocation(invocation_name, parsed):
                parsed.debug_invocations.append(self._debug_record(parsed, node, invocation_name, containing_symbol))
            lifetime_kind = self._lifetime_kind(invocation_name)
            if lifetime_kind:
                parsed.lifetime_occurrences.append(self._lifetime_record(parsed, node, invocation_name, containing_symbol, lifetime_kind))

    def _symbol_ranges(self, symbols: list[dict[str, Any]]) -> list[tuple[int, dict[str, Any]]]:
        return sorted(
            ((symbol["line"], symbol) for symbol in symbols),
            key=lambda item: (item[0], str(item[1].get("symbol") or "")),
        )

    def _containing_symbol(self, ranges: list[tuple[int, dict[str, Any]]], line: int) -> str:
        current = ""
        for symbol_line, symbol in ranges:
            if symbol_line <= line:
                current = "::".join(part for part in [symbol.get("containingType"), symbol.get("symbol")] if part)
            else:
                break
        return current

    def _invocation_name(self, function: Any, source: bytes) -> str:
        return re.sub(r"\s+", "", node_text(function, source))

    def _is_debug_invocation(self, invocation_name: str, parsed: ParsedFile) -> bool:
        method = invocation_name.rsplit(".", 1)[-1]
        if method not in DEBUG_METHODS:
            return False
        prefix = invocation_name[: -(len(method) + 1)] if "." in invocation_name else ""
        if prefix in {"Debug", "UnityEngine.Debug", "global::UnityEngine.Debug"}:
            return parsed.using_unity_engine or prefix != "Debug"
        return parsed.aliases.get(prefix) in {"UnityEngine.Debug", "global::UnityEngine.Debug"}

    def _debug_record(self, parsed: ParsedFile, node: Any, invocation_name: str, containing_symbol: str) -> dict[str, Any]:
        logging_declared = bool((parsed_repo_dependencies.get(parsed.repo) or {}).get("com.deucarian.logging"))
        logging_asmdef = bool(repo_asm_refs.get(parsed.repo, set()) & {"Deucarian.Logging", "GUID:Deucarian.Logging"})
        exception_reason = configured_exception_reason(
            parsed.governance,
            "allowedDirectDebugCalls",
            parsed.relative_path,
            containing_symbol,
        )
        classification = "Configured exception" if exception_reason is not None else classify_debug_invocation(parsed, containing_symbol)
        disposition = policy_disposition(classification, parsed.scope)
        return {
            "repository": parsed.repo,
            "packageId": parsed.package_id,
            "file": parsed.relative_path,
            "line": node.start_point[0] + 1,
            "containingSymbol": containing_symbol,
            "assembly": parsed.assembly,
            "scope": parsed.scope,
            "classification": classification,
            "invocation": invocation_name,
            "logLevel": debug_log_level(invocation_name),
            "policyDisposition": disposition,
            "policySeverity": policy_severity(disposition),
            "policyReason": exception_reason or "",
            "loggingDeclared": logging_declared,
            "asmdefReferencesLogging": logging_asmdef,
        }

    def _lifetime_kind(self, invocation_name: str) -> str:
        method = invocation_name.rsplit(".", 1)[-1]
        if invocation_name in {"Object.Destroy", "UnityEngine.Object.Destroy", "global::UnityEngine.Object.Destroy"}:
            return "direct Unity API call"
        if invocation_name in {"Object.DestroyImmediate", "UnityEngine.Object.DestroyImmediate", "global::UnityEngine.Object.DestroyImmediate"}:
            return "direct Unity API call"
        if method in DESTROY_HELPER_NAMES:
            return "helper call site"
        return ""

    def _lifetime_record(self, parsed: ParsedFile, node: Any, invocation_name: str, containing_symbol: str, kind: str) -> dict[str, Any]:
        record = {
            "repository": parsed.repo,
            "packageId": parsed.package_id,
            "file": parsed.relative_path,
            "line": node.start_point[0] + 1,
            "containingSymbol": containing_symbol,
            "assembly": parsed.assembly,
            "scope": parsed.scope,
            "classification": classify_scope(parsed.package_id, parsed.scope),
            "occurrenceKind": kind,
            "invocation": invocation_name,
        }
        exception_reason = configured_exception_reason(
            parsed.governance,
            "allowedDirectUnityObjectLifetimeCalls",
            parsed.relative_path,
            containing_symbol,
        )
        if exception_reason is not None:
            record["configuredExceptionReason"] = exception_reason
        return record


def normalize_body(body_text: str, local_names: set[str]) -> str:
    text = re.sub(r"/\*.*?\*/", " ", body_text, flags=re.S)
    text = re.sub(r"//.*", " ", text)
    text = re.sub(r'@?"(?:""|\\.|[^"\\])*"', '"S"', text)
    text = re.sub(r"'(?:\\.|[^'\\])'", "'C'", text)
    text = re.sub(r"\b\d+(?:\.\d+)?\b", "0", text)
    for name in sorted(local_names, key=len, reverse=True):
        text = re.sub(rf"\b{re.escape(name)}\b", "local", text)
    return re.sub(r"\s+", "", text).lower()


def classify_scope(package_id: str | None, scope: str) -> str:
    if package_id == "com.deucarian.bootstrap":
        return "Bootstrap exception"
    if scope == "Test":
        return "Test"
    if scope == "Sample":
        return "Sample"
    if scope in {"Runtime production", "Editor production"}:
        return scope
    return "Unknown"


def normalize_containing_symbol(value: str) -> str:
    return re.sub(r"[:.]+", ".", value or "").strip(".")


def configured_exception_reason(
    governance: dict[str, Any] | None,
    key: str,
    relative_path: str,
    containing_symbol: str = "",
) -> str | None:
    for item in (governance or {}).get(key, []) or []:
        if not isinstance(item, dict) or item.get("file") != relative_path:
            continue
        expected_symbol = normalize_containing_symbol(str(item.get("containingSymbol", "")))
        actual_symbol = normalize_containing_symbol(containing_symbol)
        if expected_symbol and not (actual_symbol == expected_symbol or actual_symbol.endswith("." + expected_symbol)):
            continue
        return str(item.get("reason") or "Reviewed exception declared in deucarian-package.json.")
    return None


def classify_debug_invocation(parsed: ParsedFile, containing_symbol: str) -> str:
    return classify_scope(parsed.package_id, parsed.scope)


def debug_log_level(invocation_name: str) -> str:
    method = invocation_name.rsplit(".", 1)[-1]
    return DEBUG_LOG_LEVELS.get(method, "Unknown")


def policy_disposition(classification: str, scope: str) -> str:
    if classification in {"Test", "Bootstrap exception", "Configured exception"}:
        return "Allowed"
    if scope in {"Runtime production", "Editor production", "Sample"}:
        return "Migrate"
    return "ReviewRequired"


def policy_severity(disposition: str) -> str:
    if disposition == "Migrate":
        return "Error"
    if disposition == "ReviewRequired":
        return "Warning"
    return "Info"


def apply_lifetime_policy(records: list[dict[str, Any]]) -> None:
    for record in records:
        disposition, reason = lifetime_policy(record)
        record["policyDisposition"] = disposition
        record["policyReason"] = reason
        record["policySeverity"] = policy_severity(disposition)


def lifetime_policy(record: dict[str, Any]) -> tuple[str, str]:
    if is_common_lifetime_implementation(record):
        return "Allowed", "Canonical Common implementation owns the Play Mode/Edit Mode UnityEngine.Object destruction capability."
    if is_common_lifetime_call_site(record):
        return "Allowed", "Production code calls the canonical Deucarian.Common lifetime API."
    if record.get("configuredExceptionReason"):
        return "Allowed", str(record["configuredExceptionReason"])
    if record.get("scope") == "Test":
        return "Allowed", "Test-only explicit Unity object teardown remains local; no shared testing package was approved."
    if record.get("scope") in {"Runtime production", "Editor production"}:
        if record.get("occurrenceKind") == "helper definition":
            return "Migrate", "Local production helper duplicates the Common lifetime capability."
        if record.get("occurrenceKind") == "helper call site":
            return "Migrate", "Production code calls a local or non-canonical lifetime helper instead of Common."
        if record.get("occurrenceKind") == "direct Unity API call":
            return "Migrate", "Production direct UnityEngine.Object destruction must route through the Common capability unless a reviewed semantic exception is recorded."
        return "ReviewRequired", "Production lifetime behavior needs semantic review."
    if record.get("scope") == "Sample":
        return "ReviewRequired", "Sample lifetime behavior should not define production capability semantics."
    return "ReviewRequired", "Lifetime finding is outside the known allowed policy scopes."


def is_common_lifetime_implementation(record: dict[str, Any]) -> bool:
    if record.get("packageId") != COMMON_PACKAGE_ID:
        return False
    if record.get("file") != "Runtime/UnityObjectUtility.cs":
        return False
    if record.get("occurrenceKind") == "helper definition" and record.get("symbol") == "DestroySafely":
        return True
    return (
        record.get("occurrenceKind") == "direct Unity API call"
        and record.get("containingSymbol") == "UnityObjectUtility::DestroySafely"
        and record.get("invocation") in LIFETIME_FORBIDDEN_SYMBOLS
    )


def is_common_lifetime_call_site(record: dict[str, Any]) -> bool:
    if record.get("packageId") == COMMON_PACKAGE_ID:
        return False
    if record.get("scope") not in {"Runtime production", "Editor production"}:
        return False
    if record.get("occurrenceKind") != "helper call site":
        return False
    invocation = record.get("invocation", "")
    return invocation == "UnityObjectUtility.DestroySafely" or invocation.endswith(".UnityObjectUtility.DestroySafely")


