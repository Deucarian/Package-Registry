def check_artifacts(args: argparse.Namespace, report: dict[str, Any]) -> int:
    with tempfile.TemporaryDirectory() as tmp:
        tmp_root = Path(tmp)
        write_artifacts(report, tmp_root, args.format)
        stale = []
        for filename in OUTPUT_FILES:
            expected = tmp_root / filename
            actual = args.output_root / filename
            if not expected.exists() and not actual.exists():
                continue
            if not expected.exists() or not actual.exists() or expected.read_bytes() != actual.read_bytes():
                stale.append(filename)
        if stale:
            print("Audit artifacts are stale:", ", ".join(stale), file=sys.stderr)
            return 1
    print("Audit artifacts are up to date.")
    return 0


def main() -> int:
    parser = build_arg_parser()
    args = parser.parse_args()
    args.output_root = args.output_root.resolve()
    if args.audit_root is None:
        args.audit_root = repo_root_default(args.output_root)
    args.audit_root = args.audit_root.resolve()
    report = build_report(args)
