def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--audit-root", type=Path, default=None, help="Root containing one checked-out repository per directory.")
    parser.add_argument("--output-root", type=Path, default=Path(__file__).resolve().parents[1], help="Directory where audit artifacts are written.")
    parser.add_argument("--organization", default="Deucarian", help="Canonical organization name for report labels.")
    parser.add_argument("--ref", default="develop", help="Git ref represented by the audit-root snapshots.")
    parser.add_argument("--authoritative", action="store_true", help="Fail if syntax-aware C# parsing is unavailable or any C# file cannot be parsed.")
    parser.add_argument("--format", choices=["all", "json", "markdown"], default="all", help="Artifact format subset to write.")
    parser.add_argument("--include-repository", action="append", default=[], help="Repository name to include; may be passed multiple times.")
